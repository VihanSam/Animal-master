public class Animal {

    String name;
    String noise;
    String move;

    //getters and setters for name

    public void setName(String inputAnimalName){

        this.name = inputAnimalName;
    }
    public String getName(){

        return this.name;
    }

    // getters and setters for noise

    public void setNoise(String inputAnimalNoise){

        this.noise = inputAnimalNoise;
    }

    public String getNoise(){

        return this.noise;
    }

    public  void  setMove(String inputAnimalMove) {

        this.move = inputAnimalMove;
    }


    public String getMove(){

        return this.move;
    }

}
